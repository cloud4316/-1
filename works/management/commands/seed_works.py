    help = 'Заполняет базу данных практическими работами'

    def handle(self, *args, **options):
        created = 0
        updated = 0
        for data in WORKS:
            work, is_new = PracticalWork.objects.update_or_create(
                title=data['title'],
                defaults=data,
            )
            if is_new:
                created += 1
            else:
                updated += 1
        self.stdout.write(self.style.SUCCESS(
            f'✓ Создано: {created}, обновлено: {updated} практических работ'
        ))
