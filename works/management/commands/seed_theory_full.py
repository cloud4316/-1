def h(tag, text, **attrs):
    a = ''.join(f' {k}="{v}"' for k, v in attrs.items())
    return f'<{tag}{a}>{text}</{tag}>'


def tip(text):   return f'<div class="tip">💡 {text}</div>'
def warn(text):  return f'<div class="warning">⚠️ {text}</div>'
def info(text):  return f'<div class="tip" style="background:#e0f2fe;border-color:#0284c7">ℹ️ {text}</div>'

def table(headers, rows):
    th = ''.join(f'<th>{c}</th>' for c in headers)
    trs = ''.join('<tr>' + ''.join(f'<td>{c}</td>' for c in r) + '</tr>' for r in rows)
    return f'<table class="theory-table"><thead><tr>{th}</tr></thead><tbody>{trs}</tbody></table>'


