    help = 'Заполняет базу расширенной теорией (4 базовых модуля, ~5 часов)'

    def handle(self, *args, **options):
        created_m = created_l = 0

        for mod_data in MODULES:
            lessons = mod_data.pop('lessons')
            module, mc = TheoryModule.objects.update_or_create(
                title=mod_data['title'],
                defaults=mod_data,
            )
            if mc:
                created_m += 1

            for les in lessons:
                _, lc = TheoryLesson.objects.update_or_create(
                    module=module,
                    title=les['title'],
                    defaults=les,
                )
                if lc:
                    created_l += 1

        from django.db.models import Sum
        total = TheoryLesson.objects.aggregate(t=Sum('estimated_minutes'))['t'] or 0
